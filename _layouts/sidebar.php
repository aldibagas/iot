<script>
 $(document).ready(function(){
      load_data();
      function load_data(page){
           $.ajax({
                url:"_helpers/data.php",
                method:"POST",
                data:{page:page},
                success:function(data){
                     $('#data').html(data);
                }
           })
      }
      $(document).on('click', '.halaman', function(e){
           var page = $(this).attr("id");
           e.preventDefault();
           load_data(page);
      });
 });
